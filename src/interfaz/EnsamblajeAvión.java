    private void detallesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_detallesActionPerformed
    try{
        int encontro = 0;
        encontro =control.buscarAvion(Integer.parseInt(txtIdAvion.getText()));
        if (encontro == 1){
           JOptionPane.showMessageDialog(null, " Id Avion: " + txtIdAvion.getText(), "  ENCONTRADO", JOptionPane.INFORMATION_MESSAGE); 
           EnsamblajePieza ventana = new EnsamblajePieza(Integer.parseInt(txtIdAvion.getText()),nombreUsuario);
           ventana.setLocationRelativeTo(null);
           ventana.setVisible(true);
           this.dispose();
        }
        else
           
                JOptionPane.showMessageDialog(null, "Id Avion: " + txtIdAvion.getText(), " NO ENCONTRADO", JOptionPane.ERROR_MESSAGE); 
                txtIdAvion.setText("");
    }catch(Exception e){
          JOptionPane.showMessageDialog(null, "Ha ocurrido un Error ", "Error", JOptionPane.ERROR_MESSAGE); 
                txtIdAvion.setText("");
      }
    }//GEN-LAST:event_detallesActionPerformed

    private void comboClienteItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_comboClienteItemStateChanged
            if (evt.getStateChange()== ItemEvent.SELECTED)  
                if ( comboCliente.getSelectedItem().equals("Todos")){
                  control.llenarTablaEnsamblajeAvion(jTable1);
              }
          else
               
                  control.llenarTablaEnsamblajeAvionCliente(jTable1, (String) comboCliente.getSelectedItem());
    }//GEN-LAST:event_comboClienteItemStateChanged

