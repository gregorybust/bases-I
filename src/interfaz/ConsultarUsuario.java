    private void consultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultarActionPerformed
       int verificar;
       int permiso=0;
       if (control.buscarPermiso(instruccion, nombreEntidad,nombreUsuario)==1){
            verificar = control.buscarUsuarioPorNombre(txtUsuario.getText());
             if (verificar == 1){
                 JOptionPane.showMessageDialog(null, "Usuario " + txtUsuario.getText(), " encontrado", JOptionPane.INFORMATION_MESSAGE); 
                 ModificarUsuario ventana = new ModificarUsuario(nombreUsuario,txtUsuario.getText());
                ventana.setLocationRelativeTo(null);
               ventana.setVisible(true);
                this.dispose();
             }
              if (verificar == 2){
               JOptionPane.showMessageDialog(null, "Usuario " + txtUsuario.getText(), " no encontrado", JOptionPane.INFORMATION_MESSAGE);
                      txtUsuario.setText("");
           }
              else
                  if (verificar ==0){
                   JOptionPane.showMessageDialog(null, "Usuario " + txtUsuario.getText(), " error al consultar", JOptionPane.ERROR_MESSAGE);
                          txtUsuario.setText("");
               }
       }
       else
        JOptionPane.showMessageDialog(null, "Acceso No Permitido "  , "Su Usuario no tiene este Privilegio ", JOptionPane.ERROR_MESSAGE); 
    }//GEN-LAST:event_consultarActionPerformed

