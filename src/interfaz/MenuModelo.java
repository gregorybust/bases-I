    private void botonAgreagarModeloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgreagarModeloActionPerformed
        instruccion="agregar";

        if (control.buscarPermiso(instruccion, nombreEntidad,nombreUsuario)==1){
          AgregarModelo ventana=new AgregarModelo(nombreUsuario);
          ventana.setLocationRelativeTo(null);
          ventana.setVisible(true);
          this.dispose();
        }
        else
             JOptionPane.showMessageDialog(null, "Su Usuario no tiene  el Privilegio  \n De agregar Modelo "  , "Acceso No Permitido", JOptionPane.ERROR_MESSAGE); 
    }//GEN-LAST:event_botonAgreagarModeloActionPerformed

