    private void AgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarActionPerformed
if ((id.getText().isEmpty() == false )&& (cantidadTxt.getText().isEmpty() == false))
{
    
  {
    
      int verificarAgregar;

   int  Pk=control.DevolverMaterialProvedorId(nombreActual,Integer.parseInt(id.getText()));
     
   String precio = control.DevolverPrecio(nombreActual,Integer.parseInt(id.getText()));
     
   int cantidad= Integer.parseInt(cantidadTxt.getText());
   int planta= control.BuscaPlantaID(plantaActual);
   int Fk = control.DevolverPedidoId(planta);
            System.out.println(cantidad);
            System.out.println(precio);
            System.out.println(nombreActual);
            System.out.println(plantaActual);
            System.out.println(Pk);
            System.out.println(Fk);
   
  verificarAgregar = control.agregarPedidoMaterial(Integer.parseInt(cantidadTxt.getText()), precio,Pk,Fk);

     
     if ((verificarAgregar == 1) ){
          JOptionPane.showMessageDialog(null, "Material Agregado" , " agregado con exito!", JOptionPane.INFORMATION_MESSAGE);
           
      }
           
      else
         if (verificarAgregar == 0)
         JOptionPane.showMessageDialog(null, "no se pudo agregar Material ", " No se pudo agregar!", JOptionPane.ERROR_MESSAGE);
   }
//    else
//         JOptionPane.showMessageDialog(null, "Proveedor", " Error", JOptionPane.ERROR_MESSAGE);
   }
   else
        JOptionPane.showMessageDialog(null, "ERROR " , " El ID y la cantidad no puede estar vacio!", JOptionPane.ERROR_MESSAGE);

    }//GEN-LAST:event_AgregarActionPerformed

