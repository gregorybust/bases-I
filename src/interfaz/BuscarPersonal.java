    private void botonBuscarPersonalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBuscarPersonalActionPerformed
       int verificar; 
      

    if (valida.validarVacio(txtCedulaBuscarPersonal,"Error") == 1     &&   valida.validarEntero(txtCedulaBuscarPersonal.getText(),"Error") == 1 ){
       verificar = control.buscarPersonal(Integer.parseInt(txtCedulaBuscarPersonal.getText()));
       if (verificar == 1){
           JOptionPane.showMessageDialog(null, "Personal " + txtCedulaBuscarPersonal.getText(), " encontrado", JOptionPane.INFORMATION_MESSAGE); 
           ModificarPersonal ventana = new ModificarPersonal(Integer.parseInt(txtCedulaBuscarPersonal.getText()));
           ventana.setLocationRelativeTo(null);
           ventana.setVisible(true);
           this.dispose(); 
       }
       else
           if (verificar == 2){
               JOptionPane.showMessageDialog(null, "Personal " + txtCedulaBuscarPersonal.getText(), " no encontrado", JOptionPane.INFORMATION_MESSAGE);
                 valida.limpia(txtCedulaBuscarPersonal);
           }
       else
               if (verificar ==0){
                   JOptionPane.showMessageDialog(null, "Conexión " + txtCedulaBuscarPersonal.getText(), " error de conexión", JOptionPane.ERROR_MESSAGE);
                        valida.limpia(txtCedulaBuscarPersonal);
               }
    }
    else
        valida.limpia(txtCedulaBuscarPersonal);
    }//GEN-LAST:event_botonBuscarPersonalActionPerformed

