    private void eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarActionPerformed
   
        if (control.buscarRol(txtRol.getText())==1){
              int verificar=control.eliminarRol(txtRol.getText());
            if (verificar==1){
               JOptionPane.showMessageDialog(null, "Se ha eliminado con Exito" , "Eliminado", JOptionPane.INFORMATION_MESSAGE);  
                txtRol.setText("");
             }
            else{
               JOptionPane.showMessageDialog(null, "Se ha eliminado con Exito" , "Eliminado", JOptionPane.ERROR_MESSAGE);  
                txtRol.setText("");
             }
        }
        else{
            JOptionPane.showMessageDialog(null, "No esta registrado el Rol" , "No Encontrado", JOptionPane.INFORMATION_MESSAGE);  
            txtRol.setText("");
        }
    }//GEN-LAST:event_eliminarActionPerformed

