    private void EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarActionPerformed
   
          int verificar; 
  Validacion valida=new Validacion();
 
     
      
          if (valida.validarVacio(nombreTxt,"Error") == 1  ){
       verificar = control.buscarProveedor(nombreTxt.getText());
       if (verificar == 1){
            
           control.eliminarProveedor(nombreTxt.getText());
     //    int fk =  control.buscarProveedorID(nombreTxt.getText());
       //    control.eliminarTelefono(fk);
           JOptionPane.showMessageDialog(null, "Proveedor " + nombreTxt.getText(), " Eliminado", JOptionPane.INFORMATION_MESSAGE);
           valida.limpia(nombreTxt);
          
       }
       else
           if (verificar == 2){
               JOptionPane.showMessageDialog(null, "Proveedor " + nombreTxt.getText(), " no encontrado", JOptionPane.INFORMATION_MESSAGE);
                valida.limpia(nombreTxt);
           }
       else
               if (verificar ==0){
                   JOptionPane.showMessageDialog(null, "Conexión " + nombreTxt.getText(), " error de conexión", JOptionPane.ERROR_MESSAGE);
                   valida.limpia(nombreTxt);
               }
   }
   else
       valida.limpia(nombreTxt);        // TODO add your handling code here:
     
   
    }//GEN-LAST:event_EliminarActionPerformed

