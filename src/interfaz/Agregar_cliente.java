    private void AgregarbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarbuttonActionPerformed
//      Validacion valida = new Validacion();  
        try{
        int municipio = control.BuscarLugarID(parroquiacombo.getSelectedItem().toString(),"Parroquia");
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String date = sdf.format(fechac.getDate());
        if ((Nombre_text.getText().isEmpty() ==false )&& (Pagina_text.getText().isEmpty()==false) && (monto_text.getText().isEmpty()==false) && (telefono_text.getText().isEmpty()==false) && (Correo_text.getText().isEmpty()==false)){
            if((control.buscarCliente(Nombre_text.getText())!=1) && (control.buscarTelefono(telefono_text.getText())!=1)){
                String otro;
                if (otro_text.getText().isEmpty()==false)
                    otro=otro_text.getText();
                else
                    otro="null";
                int check=control.agregarCliente(Nombre_text.getText(),Pagina_text.getText(),otro,date,Integer.parseInt(monto_text.getText()),municipio);
                if (check==1){
                    int idcliente=control.buscarClienteID(Nombre_text.getText());
                    check=control.agregarTelefonocliente(telefono_text.getText(),idcliente );
                    int check2=control.agregarCorreocliente(Correo_text.getText(), idcliente);
                    if (check==1 && check2==1){
                        JOptionPane.showMessageDialog(null, "Cliente " +Nombre_text.getText(), " agregado con exito!", JOptionPane.INFORMATION_MESSAGE);
                        Menu_cliente ventana = new Menu_cliente(nombreUsuario);
                        ventana.setLocationRelativeTo(null);
                        ventana.setVisible(true);
           this.dispose();
                    }
                }
            }
            else
                JOptionPane.showMessageDialog(null, "Cliente o numero ya registrados!" + Nombre_text.getText(), " Error", JOptionPane.ERROR_MESSAGE);
        }
        else
                JOptionPane.showMessageDialog(null, "Falta llenar campos obligatorios", " Error", JOptionPane.ERROR_MESSAGE);
      }catch(Exception e){
        JOptionPane.showMessageDialog(null, "Ocurrio un error!", " Error", JOptionPane.ERROR_MESSAGE);
      }
    }//GEN-LAST:event_AgregarbuttonActionPerformed

