       public ModificarUsuario(int idRrol,String nombreUsuario,String clave,String nombreUsuarioActual) {
        initComponents();
        this.setLocationRelativeTo(null);
        this.setResizable(false);
        txtRol.setEnabled(false);
        txtUsuario.setText(nombreUsuario);
        txtClave.setText(clave);
        String nombreRol=control.devolverNombreRolId(idRrol);
        txtRol.setText(nombreRol);
        this.idRol=idRrol;
        this.nombreUsuarioActual=nombreUsuarioActual;
        this.nombreUsuarioModificar=nombreUsuario;
    }
     public ModificarUsuario(String nombreUsuarioActual,String nombreUsuarioModificar) {
        initComponents();
        this.setLocationRelativeTo(null);
        this.setResizable(false);
       
        txtRol.setEnabled(false);
      
        control.llenarVentaModificarUsuario(nombreUsuarioModificar, txtUsuario, txtClave, txtRol);
        idRol=control.devolverIdRol(txtRol.getText());
        this.nombreUsuarioActual=nombreUsuarioActual;
        this.nombreUsuarioModificar=nombreUsuarioModificar;
    }

