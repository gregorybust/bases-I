    private void agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarActionPerformed
       
        int verificar=control.modificarUsuario(nombreUsuarioModificar,txtUsuario.getText(),txtClave.getText(),idRol);
        if (verificar==1){
          JOptionPane.showMessageDialog(null, "Se ha modificado con exito "  , "Modificado ", JOptionPane.INFORMATION_MESSAGE); 
           Menu_Usuario ventana = new Menu_Usuario(nombreUsuarioActual);
           ventana.setLocationRelativeTo(null);
           ventana.setVisible(true);
           this.dispose(); 
        }
        else
            JOptionPane.showMessageDialog(null, "No se puedo Modificar "  , "No  Modificado ", JOptionPane.ERROR_MESSAGE); 
    }//GEN-LAST:event_agregarActionPerformed

